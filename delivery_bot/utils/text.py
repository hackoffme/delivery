def split_text(text: str, l=4000):
    if len(text) <= l:
        return [text]
    ret = []
    text = text.split('\n')
    message = ''
    for item in text:
        if len(message)+len(item) >= l:
            ret.append(message)
            message = ''
        message += f'{item}\n'
    message and ret.append(message)
    return ret
